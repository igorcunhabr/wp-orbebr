// Funcionalidade dos botões flutuantes
document.addEventListener('DOMContentLoaded', function() {
    const scrollToTopBtn = document.getElementById('scroll-to-top');
    
    // Funcionalidade do botão scroll to top
    if (scrollToTopBtn) {
        scrollToTopBtn.addEventListener('click', function(e) {
            e.preventDefault();
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
    }
    
    // Mostrar/ocultar botão scroll to top baseado na posição da página
    function toggleScrollButton() {
        if (window.pageYOffset > 300) {
            scrollToTopBtn.style.opacity = '1';
            scrollToTopBtn.style.visibility = 'visible';
        } else {
            scrollToTopBtn.style.opacity = '0';
            scrollToTopBtn.style.visibility = 'hidden';
        }
    }
    
    // Inicializar estado do botão
    if (scrollToTopBtn) {
        scrollToTopBtn.style.transition = 'opacity 0.3s ease, visibility 0.3s ease';
        toggleScrollButton();
        window.addEventListener('scroll', toggleScrollButton);
    }
});
